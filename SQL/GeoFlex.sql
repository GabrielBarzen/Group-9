    START TRANSACTION;
    insert into route(title, description, type) values (in_title,in_description,in_type);
    select id into route_id from route where route.type = in_type && route.description = in_description && route.title = in_title;
    select code into route_code from route where route.type = in_type && route.description = in_description && route.title = in_title;

    SET @x = 0;
    REPEAT
        SET @x = @x + 1;
        insert into location(route, name,location_index, text_info) values (route_id,@x,@x,'Replace me');
        SET @i = (SELECT id FROM location WHERE route = route_id AND location_index = @x);
        INSERT INTO location_position(location_id) VALUES (@i);
        UNTIL @x >= num_locations
    END REPEAT;
    insert into location(route, name, text_info,last_location) values (route_id,'Last location','Replace me',true);


    COMMIT;
end ;;
